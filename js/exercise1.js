function addNum(num1, num2){
    return num1 + num2
}

function subNum(num1, num2){
    return num1 - num2
}

function mulNum(num1, num2){
    return num1 * num2
}

function divNum(num1, num2){
    return num1 / num2
}

console.log('add Number : ', addNum(10, '20'))
console.log('sub Number : ', subNum(10, '20'))
console.log('mul Number : ', mulNum(10, '20'))
console.log('div Number : ', divNum(10, '20'))

